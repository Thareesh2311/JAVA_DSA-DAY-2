package DAY_2;

public class dowhile_loop {
    public static void main(String[] args) {
        // do-while loop syntax
        // do{  
        //     // code to be executed  
        // }while(condition);  

        int i = 1;
        do{
            System.out.println(i);
            i++;
        }while(i<=5);
    }
}
