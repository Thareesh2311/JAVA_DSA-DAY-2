package DAY_2;

public class break_statement {
    public static void main(String[] args){
        // break Statement definition: A break statement is used to exit a loop or switch statement prematurely when a certain condition is met.
        // break statement syntax
        // break;
        for(int i=0;i<=10;i++){
            if(i==2) break;
            System.out.println(i);
        }
    }
}
